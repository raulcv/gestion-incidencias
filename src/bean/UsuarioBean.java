package bean;

public class UsuarioBean {
	
	private int codigoU;
	private String usuarioU;
	private String passwordU;
	private String nombreU;
	private String apellidoU;
	private String pregunta;
	private String respuesta;
	private String estado;
	private String fechareg;
	private String fechaUltMod;
	
	
	public int getCodigoU() {
		return codigoU;
	}
	public void setCodigoU(int codigoU) {
		this.codigoU = codigoU;
	}
	public String getUsuarioU() {
		return usuarioU;
	}
	public void setUsuarioU(String usuarioU) {
		this.usuarioU = usuarioU;
	}
	public String getPasswordU() {
		return passwordU;
	}
	public void setPasswordU(String passwordU) {
		this.passwordU = passwordU;
	}
	public String getNombreU() {
		return nombreU;
	}
	public void setNombreU(String nombreU) {
		this.nombreU = nombreU;
	}
	public String getApellidoU() {
		return apellidoU;
	}
	public void setApellidoU(String apellidoU) {
		this.apellidoU = apellidoU;
	}
	public String getPregunta() {
		return pregunta;
	}
	public void setPregunta(String pregunta) {
		this.pregunta = pregunta;
	}
	public String getRespuesta() {
		return respuesta;
	}
	public void setRespuesta(String respuesta) {
		this.respuesta = respuesta;
	}
	public String getEstado() {
		return estado;
	}
	public void setEstado(String estado) {
		this.estado = estado;
	}
	public String getFechareg() {
		return fechareg;
	}
	public void setFechareg(String fechareg) {
		this.fechareg = fechareg;
	}
	public String getFechaUltMod() {
		return fechaUltMod;
	}
	public void setFechaUltMod(String fechaUltMod) {
		this.fechaUltMod = fechaUltMod;
	}
	
}
